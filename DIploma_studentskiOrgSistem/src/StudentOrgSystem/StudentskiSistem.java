    public void updateTable(boolean prikaz) throws SQLException {

        Connection myConn = null;
        Statement myStmt = null;
        ResultSet myRs = null;

        String user = "root";
        String pass = "root";

        try {
            // 1. Get a connection to database
            myConn = DriverManager.getConnection("jdbc:mysql://localhost:3306/student", user, pass);

            // 2. Create a statement
            myStmt = myConn.createStatement();

            // 3. Execute SQL query
            myRs = myStmt.executeQuery("select * from student.izpitniroki");

            // 4. Process the result set
            while (myRs.next()) {
                String id = String.valueOf(myRs.getInt("ID_rok"));
                String predmet = myRs.getString("Predmet");
                String DatumRoka = myRs.getString("DatumRoka");
                String lokacija = myRs.getString("Lokacija");
                String casPisanja = myRs.getString("CasPisanja");

                if (!arrayId_izpitniRoki.contains(id)) {
                    if (prikaz ==true){
                    String tbData[] = {predmet, DatumRoka, lokacija, casPisanja};
                    DefaultTableModel tblModel = (DefaultTableModel) table_IzpitniRoki.getModel();
                    tblModel.addRow(tbData);
                   }
                if (!arrayId_izpitniRoki1.contains(id))
                   arrayId_izpitniRoki1.add(id);
                }
                if (prikaz==true){
                arrayId_izpitniRoki.add(id);
                }
            }

        } catch (Exception exc) {
            exc.printStackTrace();
        } finally {
            if (myRs != null) {
                myRs.close();
            }

            if (myStmt != null) {
                myStmt.close();
            }

            if (myConn != null) {
                myConn.close();
            }

        }
    }

